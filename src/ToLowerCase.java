
public class ToLowerCase {

	public static void main(String[] args) {
		String str = "I love JAVA";
		String result = str.toLowerCase();
		System.out.println("Result: " + result);

		str = "TEXT 1 2 3";
		result = str.toLowerCase();
		System.out.println("Result: " + result);

		str = "vertex";
		result = str.toLowerCase();
		System.out.println("Result: " + result);
	}

}
